package lista_oop_4;

public class Questao2classe 
{
	static public double somar(double num1, double num2) 
	{
		return num1 + num2;
	}
	
	static public double subtrair(int num1, int num2) 
	{
		return num1 - num2;
	}
	
	static public double multiplicar(double num1, double num2) 
	{
		return num1 * num2;
	}
	
	static public double dividir(double num1, double num2)
	{
		return num1 / num2;
	}
	
	static public double elevarPotencia(double num1, double num2) 
	{
		return (double) Math.pow(num1, num2);
	}
}
